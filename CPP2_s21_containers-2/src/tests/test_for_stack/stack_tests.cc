using namespace s21;

// Тест конструктора по умолчанию
TEST(MyStackTest, DefaultConstructor) {
  stack<int> s;
  ASSERT_TRUE(s.empty());
  ASSERT_EQ(0, s.size());
}

// Тест конструктора с инициализацией
TEST(MyStackTest, InitializerListConstructor) {
  stack<int> s{1, 2, 3};
  ASSERT_FALSE(s.empty());
  ASSERT_EQ(3, s.size());
  ASSERT_EQ(3, s.top());
}

// Тест копирующего конструктора
TEST(MyStackTest, CopyConstructor) {
  stack<int> s1{1, 2, 3};
  stack<int> s2(s1);
  ASSERT_EQ(s1.size(), s2.size());
  ASSERT_EQ(s1.top(), s2.top());
}

// Тест перемещающего конструктора
TEST(MyStackTest, MoveConstructor) {
  stack<int> s1{1, 2, 3};
  stack<int> s2(std::move(s1));
  ASSERT_EQ(3, s2.size());
  ASSERT_EQ(3, s2.top());
  ASSERT_TRUE(s1.empty());
}

// Тест оператора присваивания перемещением
TEST(MyStackTest, MoveAssignmentOperator) {
  stack<int> s1{1, 2, 3};
  stack<int> s2;
  s2 = std::move(s1);
  ASSERT_EQ(3, s2.size());
  ASSERT_EQ(3, s2.top());
  ASSERT_TRUE(s1.empty());
}

TEST(MyStackTest, EmptyMethod) {
  stack<int> s;
  ASSERT_TRUE(s.empty());
  s.push(42);
  ASSERT_FALSE(s.empty());
}

TEST(MyStackTest, SizeMethod) {
  stack<int> s{1, 2, 3};
  ASSERT_EQ(3, s.size());
  s.pop();
  ASSERT_EQ(2, s.size());
  s.push(4);
  ASSERT_EQ(3, s.size());
}

TEST(MyStackTest, TopMethod) {
  stack<int> s{1, 2, 3};
  ASSERT_EQ(3, s.top());
  s.pop();
  ASSERT_EQ(2, s.top());
}

TEST(MyStackTest, PushMethod) {
  stack<int> s{1, 2, 3};
  s.push(4);
  ASSERT_EQ(4, s.top());
  ASSERT_EQ(4, s.size());
}

TEST(MyStackTest, PopMethod) {
  stack<int> s{1, 2, 3};
  s.pop();
  ASSERT_EQ(2, s.top());
  ASSERT_EQ(2, s.size());
}

TEST(MyStackTest, SwapMethod) {
  stack<int> s1{1, 2};
  stack<int> s2{3, 4};
  s1.swap(s2);
  ASSERT_EQ(2, s2.top());
  ASSERT_EQ(2, s2.size());
  ASSERT_EQ(4, s1.top());
  ASSERT_EQ(2, s1.size());
}

